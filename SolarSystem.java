    private void FinishBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinishBtnActionPerformed
         if((MerRB.isSelected()==false&&MarRB.isSelected()==false)||(VenuRB.isSelected()==false&&JupRB.isSelected()==false)||
            (JupiRB.isSelected()==false&&SatRB.isSelected()==false)||(VenusRB.isSelected()==false&&NeptRB.isSelected()==false)||
            (EarRB.isSelected()==false&&MercRB.isSelected()==false)||(MersRB.isSelected()==false&&
            MercuryRB.isSelected()==false))
         {
            Doit.setVisible(true);
         }
         else
         {
            int mark=0;
            if(MerRB.isSelected())
               mark=mark+5;
            if(VenuRB.isSelected())
               mark=mark+5;
            if(JupiRB.isSelected())
               mark=mark+5;
            if(NeptRB.isSelected())
               mark=mark+5;
            if(EarRB.isSelected())
                mark=mark+5;
            if(MersRB.isSelected())
                mark=mark+5;
            if(MarRB.isSelected())
                mark=mark+0;
            if(JupRB.isSelected())
                mark=mark+0;
            if(SatRB.isSelected())
                mark=mark+0;
            if(VenusRB.isSelected())
                mark=mark+0;
            if(MercRB.isSelected())
                mark=mark+0;
            if(MercuryRB.isSelected())
                mark=mark+0;
            if(mark==30)
            {
                JOptionPane.showMessageDialog(this,"GREAT!YOU HAVE SCORED "+mark+
                        " ON 30!!!<<YOU ARE A GENEIUS>>");
                Bye.setVisible(true);
            }
            else if(mark>=15&&mark!=30)
            {
                JOptionPane.showMessageDialog(this,"GOOD!YOU HAVE SCORED "+mark+
                        " ON 30!!! <<YOU HAVE GOT A SLEEPING GENIUS IN YOU>>");
                Bye.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(this,"OH MY GOD!YOU HAVE ONLY SCORED "+mark+
                        " ON 30!!!<<YOU ARE OF NO USE>> HA....HA...JUST KIDDING.");
                Bye.setVisible(true);
            } 
            }
         
    }//GEN-LAST:event_FinishBtnActionPerformed

